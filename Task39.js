"use strict";
function city_country(city, country) {
    return `${city}, ${country}`;
}
const lahore_pakistan = city_country("Lahore", "Pakistan");
console.log(lahore_pakistan);
const new_york_usa = city_country("New York", "USA");
console.log(new_york_usa);
const tokyo_japan = city_country("Tokyo", "Japan");
console.log(tokyo_japan);
